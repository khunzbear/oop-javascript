// OOP javascript

// create class

class User {
  // property
  #name = "name";
  #password = 123;

  // params constructor

  constructor(n, p) {
    console.log("Starting  Constructor... ");
    this.#name = n;
    this.#password = p;
  }
  //   method
  showdetail() {
    console.log(`Username ${this.#name} , Password ${this.#password}`);
  }
  //   setter getter

  setName(newName) {
    this.#name = newName;
  }
  getName() {
    return this.#name;
  }
}

